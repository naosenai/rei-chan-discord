def retrieve_question(qtype, category = None):
    qtype = {"TRUTH": truths, "DARE": dares, "RANDOM": random.choice([truths, dares])}[qtype]
    category = category if category in qtype.keys() else random.choice(list(qtype.keys()))
    question = random.choice(qtype[category])
    return (question, f"{category} #{qtype[category].index(question) + 1}")

